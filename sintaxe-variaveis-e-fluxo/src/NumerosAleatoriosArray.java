
public class NumerosAleatoriosArray {

	public static void main(String[] args) {
		
		//array lista = {10,20,30,40,50,60};
	double []numeros  = {5, 50, 40, 30, 10, 2};
		
		double numero = (int)Math.floor(Math.random() * numeros.length);
		//imprime a posição zero do elemento do array
		System.out.println(numeros [0]);
		
		// imprime a posição aleatoria do elemento do array
		System.out.println(numero);
		
		//imprime a posição aleatoria do elemento do array
		
				
	}

}
